// Helper to get related pages data
export function getRelatedExpertisePages(slugs: string[]): Array<{
  slug: string;
  title: string;
  description: string;
  badge: string;
}> {
  return slugs
    .filter((slug) => expertisePages[slug])
    .map((slug) => {
      const page = expertisePages[slug];
      return {
        slug: page.slug,
        title: page.hero.headline + " " + page.hero.headlineAccent,
        description: page.hero.tagline,
        badge: page.hero.badge,
      };
    });
}

// Get all expertise slugs for static generation
export function getAllExpertiseSlugs(): string[] {
  return Object.keys(expertisePages);
}

// Get page by slug
export function getExpertisePage(slug: string): ExpertisePageData | undefined {
  return expertisePages[slug];
}
