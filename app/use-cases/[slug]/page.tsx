const defaultIcon = UseCaseIcons.sparkles;

export async function generateStaticParams() {
  return getAllUseCaseSlugsFromContent().map((slug) => ({ slug }));
}

export async function generateMetadata({
  params,
}: PageProps): Promise<Metadata> {
  const { slug } = await params;
  const page = getUseCaseForListing(slug);

  if (!page) {
    return {
      title: "Use Case | Procedure",
    };
  }

  return {
    title: page.meta.title,
    description: page.meta.description,
    openGraph: {
      title: page.meta.title,
      description: page.meta.description,
      type: "website",
    },
  };
}

export default async function UseCasePage({ params }: PageProps) {
  const { slug } = await params;
  const pageData = getUseCaseForListing(slug);

  if (!pageData) {
    notFound();
  }

  // Map architecture steps with icons
  const architectureSteps = pageData.architecture.steps.map((step) => ({
    ...step,
    icon: UseCaseIcons[step.icon] || defaultIcon,
  }));

  // Map feature items with icons
  const featureItems = pageData.features.items.map((item) => ({
    ...item,
    icon: UseCaseIcons[item.icon] || defaultIcon,
  }));

  return (
    <main className="min-h-screen">
      {/* JSON-LD Structured Data for FAQs */}
      {pageData.faqs.length > 0 && (
        <script
          type="application/ld+json"
          dangerouslySetInnerHTML={{
            __html: JSON.stringify({
              "@context": "https://schema.org",
              "@type": "FAQPage",
              mainEntity: pageData.faqs.map((faq) => ({
                "@type": "Question",
                name: faq.question,
                acceptedAnswer: {
                  "@type": "Answer",
                  text: faq.answer,
                },
              })),
            }),
          }}
        />
      )}

      <UseCaseHero
        badge={pageData.hero.badge}
        headline={pageData.hero.headline}
        highlightedText={pageData.hero.highlightedText}
        tagline={pageData.hero.tagline}
        description={pageData.hero.description}
      />

      <ProblemSolutionSection
        before={pageData.problemSolution.before}
        after={pageData.problemSolution.after}
      />

      <ArchitectureDiagram
        title={pageData.architecture.title}
        subtitle={pageData.architecture.subtitle}
        steps={architectureSteps}
      />

      <FeaturesBreakdown
        title={pageData.features.title}
        subtitle={pageData.features.subtitle}
        features={featureItems}
      />

      <SuccessMetrics
        title="Proven Results"
        subtitle="Real outcomes from our implementations"
        metrics={pageData.metrics}
      />

      <WhyProcedure
        title={pageData.whyProcedure.title}
        points={pageData.whyProcedure.points}
      />

      {pageData.faqs.length > 0 && <FAQSection faqs={pageData.faqs} />}

      <UseCaseCTA
        headline={pageData.cta.headline}
        description={pageData.cta.description}
        primaryCTA={pageData.cta.primaryCTA}
        secondaryCTA={pageData.cta.secondaryCTA}
      />
    </main>
  );
}
